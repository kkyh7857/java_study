
public class ForExam01 {

	public static void main(String[] args) {
		// for문을 활용하여 21에서 57까지 숫자 중 홀수만 출력하기
		
		for(int num = 21; num<=57; num+=2) {
			System.out.print(num+" ");
		}
		// for문을 활용하여 96에서 53까지 출력하기
		System.out.println();
		for(int num = 96; num>=53; num--) {
			System.out.print(num+" ");
		}

	}

}
